    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
            
            fns = jLabel1.getText();
            
            double result;
            
            switch ( operator )
            {
                case 1 :
                    
                        sns = fns.substring(fns.indexOf("+")+1, fns.length() );
                        snd = Double.parseDouble(sns);
                    
                        result = fnd + snd ;
                        jLabel1.setText("="+String.valueOf(result));
                        
                    break;
                
                case 2 :
                        
                        sns = fns.substring(fns.indexOf("-")+1, fns.length() );
                        snd = Double.parseDouble(sns);
                    
                        result = fnd - snd ;
                        jLabel1.setText("="+String.valueOf(result));
                        
                    break;
                
                case 3 :
                        
                        sns = fns.substring(fns.indexOf("*")+1, fns.length() );
                        snd = Double.parseDouble(sns);
                    
                        result = fnd * snd ;
                        jLabel1.setText("="+String.valueOf(result));
                        
                    break;
                   
                case 4 :
                  
                        sns = fns.substring(fns.indexOf("/")+1, fns.length() );
                        snd = Double.parseDouble(sns);
                    
                        result = fnd / snd ;
                        jLabel1.setText("="+String.valueOf(result));
                    
                    break;
                    
            }
    }//GEN-LAST:event_jButton15ActionPerformed

